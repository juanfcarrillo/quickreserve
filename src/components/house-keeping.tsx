'use client'

import { CleaningModal } from '@/app/housekeeping/CleaningModal'
import { Avatar, AvatarFallback, AvatarImage } from '@/components/ui/avatar'
import { Button } from '@/components/ui/button'
import { Card } from '@/components/ui/card'
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table'
import { Cleaning, CleaningStatus } from '@/models/Cleaning'
import { suscribeToCleaningService, updateCleaningService } from '@/services/CleaningService'
import { useEffect, useState } from 'react'
import { SelectStatus } from './utils/SelectStatus'
import { SelectCleaning } from '@/app/housekeeping/SelectCleaning'

export function HouseKeeping() {
  const [cleanings, setCleanings] = useState<Cleaning[]>([])

  useEffect(() => {
    suscribeToCleaningService((cleanings) => setCleanings(cleanings))
  }, [])

  async function handleStatusChange(cleaning: Cleaning, cleaningStatus: CleaningStatus) {
    const newCleaning = { ...cleaning, status: cleaningStatus }

    await updateCleaningService(newCleaning)
  }

  return (
    <div className="grid gap-6">
      <div className="flex items-center justify-between">
        <h1 className="text-2xl font-bold">Housekeeping</h1>
        <CleaningModal />
      </div>
      <Card>
        <Table>
          <TableHeader>
            <TableRow>
              <TableHead>Room</TableHead>
              <TableHead>Status</TableHead>
              <TableHead>Assigned To</TableHead>
              <TableHead>Due Date</TableHead>
              <TableHead className="text-right">Actions</TableHead>
            </TableRow>
          </TableHeader>
          <TableBody>
            {
              cleanings.map((cleaning) => (
                <TableRow key={cleaning.id}>
                  <TableCell>
                    <div className="font-medium">{cleaning.room.name}</div>
                  </TableCell>
                  <TableCell>
                    <SelectCleaning status={cleaning.status as CleaningStatus} onChange={(status: CleaningStatus) => handleStatusChange(cleaning, status)} />
                  </TableCell>
                  <TableCell>
                    <div className="flex items-center gap-2">
                      <Avatar className="w-8 h-8 border">
                        <AvatarImage src="/placeholder-user.jpg" />
                        <AvatarFallback>{cleaning.asignedTo}</AvatarFallback>
                      </Avatar>
                      <div>
                        <div className="font-medium">{cleaning.asignedTo}</div>
                        <div className="text-xs text-muted-foreground">Housekeeper</div>
                      </div>
                    </div>
                  </TableCell>
                  <TableCell>
                    <div className="font-medium">{cleaning.dueDate.toLocaleDateString()}</div>
                  </TableCell>
                  <TableCell className="text-right">
                    <div className="flex items-center justify-end gap-2">
                      <Button variant="ghost" size="icon">
                        <TrashIcon className="w-5 h-5" />
                        <span className="sr-only">Delete</span>
                      </Button>
                    </div>
                  </TableCell>
                </TableRow>
              ))
            }
          </TableBody>
        </Table>
      </Card>
    </div>
  )
}

